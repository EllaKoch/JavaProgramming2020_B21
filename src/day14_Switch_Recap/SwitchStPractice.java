package day14_Switch_Recap;

public class SwitchStPractice {
    public static void main(String[] args) {
        char a = 'B';
        String result = "";

        switch(a){
            case 'A':
                result = "One";
                break;

            case 'B':
                result = "Two";
                break;

        }
        System.out.println(result);


    }
}
