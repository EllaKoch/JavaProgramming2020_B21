package day19ForLoop;

public class ForLoopIntro {
    public static void main(String[] args) {

        for(int i = 1; i <= 5 ; i++){// ++i
            System.out.println("hello");
        }

        System.out.println("====================================");

        for(int i = 5; i > 0; i-- ){
            System.out.println("Elvira");
        }

    }
}
