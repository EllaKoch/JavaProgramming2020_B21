package day13_SwitchStatement;

public class SwitchStetement {
    public static void main(String[] args) {

        switch (2){ //no boolean


            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;

            case 3:
                System.out.println("Three");
                break;

            default:
                System.out.println("Invalid case");
                break;

        }



    }
}
